// Dates

let myDate = new Date()
console.log(myDate);

console.log(myDate.toString()); //Fri Jun 05 2026 21:14:58 GMT+0530 (India Standard Time)

console.log(myDate.toDateString()); //Fri Jun 05 2026
console.log(myDate.toISOString()); //2026-06-05T15:46:59.345Z
console.log(myDate.toLocaleString()); //5/6/2026, 9:16:59 pm


